package com.atw.weight.bean.weight;

/**
 * 发货单位
 * 
 * @author haibara
 *
 */

public class Sender {
	private int id;
	private String code;
	private String sender;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getSender() {
		return sender;
	}

	public void setSender(String sender) {
		this.sender = sender;
	}
}
